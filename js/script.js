const audioPlayer = document.getElementById('audioPlayer');
const playBtn = document.getElementById('playBtn');
const prevBtn = document.getElementById('prevBtn');
const nextBtn = document.getElementById('nextBtn');
const muteBtn = document.getElementById('muteBtn');
const progressBar = document.getElementById('progressBar');
const currentTimeEl = document.getElementById('currentTime');
const totalTimeEl = document.getElementById('totalTime');
const volumeSlider = document.getElementById('volumeSlider');
const songTitle = document.getElementById('songTitle');
const artistName = document.getElementById('artistName');
const albumCover = document.getElementById('albumCover');
const lyricsContent = document.getElementById('lyricsContent');
const songCards = document.querySelectorAll('.song-card');


let currentSongIndex = 0;
let isPlaying = false;


function formatTime(seconds) {
    const min = Math.floor(seconds / 60);
    const sec = Math.floor(seconds % 60);
    return `${min}:${sec < 10 ? '0' : ''}${sec}`;
}


audioPlayer.addEventListener('loadedmetadata', () => {
    totalTimeEl.textContent = formatTime(audioPlayer.duration);
});


function updateProgress() {
    const percent = (audioPlayer.currentTime / audioPlayer.duration) * 100;
    progressBar.value = percent || 0;
    currentTimeEl.textContent = formatTime(audioPlayer.currentTime);
}


function setProgress() {
    const width = (progressBar.value / 100) * audioPlayer.duration;
    audioPlayer.currentTime = width;
    updateLyricsDisplay();
}


function updateLyricsDisplay() {
    const currentSong = songs[currentSongIndex];


    const lyricsLines = currentSong.lyrics.split('\n');
    const lyricsHTML = lyricsLines.map(line => `<p>${line}</p>`).join('');

    lyricsContent.innerHTML = lyricsHTML;
}


function playSong() {
    isPlaying = true;
    playBtn.innerHTML = '<i class="fas fa-pause"></i>';
    audioPlayer.play();
}


function pauseSong() {
    isPlaying = false;
    playBtn.innerHTML = '<i class="fas fa-play"></i>';
    audioPlayer.pause();
}


function loadSong(index) {
    const song = songs[index];
    songTitle.textContent = song.title;
    artistName.textContent = song.artist;
    albumCover.src = song.cover;


    currentTimeEl.textContent = '0:00';
    totalTimeEl.textContent = '0:00';


    audioPlayer.onloadedmetadata = function() {
        totalTimeEl.textContent = formatTime(audioPlayer.duration);
    };

    audioPlayer.src = song.src;


    progressBar.value = 0;


    const lyricsLines = song.lyrics.split('\n');
    const lyricsHTML = lyricsLines.map(line => `<p>${line}</p>`).join('');
    lyricsContent.innerHTML = lyricsHTML;
}


function nextSong() {
    currentSongIndex++;
    if (currentSongIndex > songs.length - 1) {
        currentSongIndex = 0;
    }
    loadSong(currentSongIndex);
    if (isPlaying) {
        playSong();
    }
}


function prevSong() {
    currentSongIndex--;
    if (currentSongIndex < 0) {
        currentSongIndex = songs.length - 1;
    }
    loadSong(currentSongIndex);
    if (isPlaying) {
        playSong();
    }
}


function updateVolume() {
    audioPlayer.volume = volumeSlider.value / 100;
    const volumeIcon = muteBtn.innerHTML.includes('fa-volume-up') ? 
        '<i class="fas fa-volume-up"></i>' : 
        '<i class="fas fa-volume-mute"></i>';
    muteBtn.innerHTML = volumeIcon;
}


function toggleMute() {
    if (audioPlayer.volume > 0) {
        audioPlayer.volume = 0;
        volumeSlider.value = 0;
        muteBtn.innerHTML = '<i class="fas fa-volume-mute"></i>';
    } else {
        audioPlayer.volume = 0.5;
        volumeSlider.value = 50;
        muteBtn.innerHTML = '<i class="fas fa-volume-up"></i>';
    }
}


function setSongFromList(index) {
    currentSongIndex = index;
    loadSong(currentSongIndex);
    if (isPlaying) {
        playSong();
    } else {
        playSong();
    }
}


playBtn.addEventListener('click', () => {
    if (isPlaying) {
        pauseSong();
    } else {
        playSong();
    }
});

nextBtn.addEventListener('click', nextSong);
prevBtn.addEventListener('click', prevSong);
muteBtn.addEventListener('click', toggleMute);
volumeSlider.addEventListener('input', updateVolume);

progressBar.addEventListener('input', setProgress);
audioPlayer.addEventListener('timeupdate', updateProgress);
audioPlayer.addEventListener('ended', nextSong);


songCards.forEach((card, index) => {
    card.addEventListener('click', () => {
        setSongFromList(index);
    });
});


loadSong(currentSongIndex);

